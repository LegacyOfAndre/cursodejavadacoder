package basicjavaproject.operadores;

public class Aritmeticos {

	public static void main(String[] args) {
		
		System.out.println(3+3);
		
		var a = 32.4;
		double c = 3;
		
		System.out.println(a + c);
		System.out.println(a - c);
		System.out.println(a * c);
		System.out.println(a / c);
		
		int x = 8;
		int y = 4;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		System.out.println(x % y);
		System.out.println(8 % 4);
	}
}
